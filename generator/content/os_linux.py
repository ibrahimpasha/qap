def _process_scheduling_memory() -> TopicSection:
    explanation = (
        "### Process Scheduling and Memory Management\n\n"
        "**CPU Scheduling** determines which process runs on the CPU and for "
        "how long. The kernel's scheduler makes these decisions.\n\n"
        "**Common scheduling algorithms:**\n"
        "- **First-Come, First-Served (FCFS):** Processes run in arrival order. "
        "Simple but can cause the *convoy effect* (short jobs wait behind long ones).\n"
        "- **Shortest Job First (SJF):** Runs the shortest job next. Optimal "
        "average wait time but requires knowing job lengths in advance.\n"
        "- **Round Robin (RR):** Each process gets a fixed *time quantum* "
        "(e.g., 10ms). After the quantum expires, the process goes to the "
        "back of the queue. Fair and responsive.\n"
        "- **Priority Scheduling:** Each process has a priority. Higher-priority "
        "processes run first. Risk of *starvation* (low-priority processes "
        "never run) — solved by *aging*.\n\n"
        "**Linux uses the Completely Fair Scheduler (CFS)** — a weighted "
        "fair-queuing scheduler that gives each process a fair share of CPU "
        "time based on its nice value.\n\n"
        "**Memory Management:**\n"
        "- **Virtual Memory:** Each process sees its own address space, mapped "
        "to physical memory by the MMU (Memory Management Unit).\n"
        "- **Paging:** Memory is divided into fixed-size *pages* (typically 4KB). "
        "The OS maps virtual pages to physical *frames*.\n"
        "- **Page Fault:** Occurs when a process accesses a page not in physical "
        "memory. The OS loads it from disk (swap).\n"
        "- **Swap:** Disk space used as overflow when physical RAM is full. "
        "Swapping is slow — excessive swapping is called *thrashing*."
    )

    examples = [
        (
            "# --- Round Robin scheduling simulation ---\n"
            "# Demonstrates how RR scheduling works with a time quantum\n\n"
            "from collections import deque\n\n"
            "def round_robin(processes: list[tuple[str, int]], quantum: int) -> list[str]:\n"
            "    \"\"\"\n"
            "    Simulate Round Robin scheduling.\n"
            "    processes: list of (name, burst_time) tuples\n"
            "    quantum: time slice for each process\n"
            "    Returns: execution order log\n"
            "    \"\"\"\n"
            "    queue = deque([(name, burst) for name, burst in processes])\n"
            "    log = []  # execution order\n"
            "    time = 0\n\n"
            "    while queue:\n"
            "        name, remaining = queue.popleft()\n"
            "        # Process runs for min(quantum, remaining) time\n"
            "        run_time = min(quantum, remaining)\n"
            "        time += run_time\n"
            "        remaining -= run_time\n"
            "        log.append(f'{name}(ran {run_time}ms, t={time}ms)')\n\n"
            "        if remaining > 0:\n"
            "            queue.append((name, remaining))  # back to queue\n\n"
            "    return log\n\n"
            "# Example: 3 processes with quantum = 4ms\n"
            "processes = [('P1', 10), ('P2', 4), ('P3', 6)]\n"
            "for entry in round_robin(processes, quantum=4):\n"
            "    print(entry)\n"
            "# P1(ran 4ms, t=4ms)\n"
            "# P2(ran 4ms, t=8ms)    <- P2 finishes\n"
            "# P3(ran 4ms, t=12ms)\n"
            "# P1(ran 4ms, t=16ms)\n"
            "# P3(ran 2ms, t=18ms)   <- P3 finishes\n"
            "# P1(ran 2ms, t=20ms)   <- P1 finishes"
        ),
        (
            "# --- Checking memory and swap usage ---\n"
            "import textwrap\n\n"
            "mem_output = textwrap.dedent('''\n"
            "    $ free -h\n"
            "                  total    used    free   shared  buff/cache  available\n"
            "    Mem:           16Gi    8.2Gi   2.1Gi   512Mi      5.7Gi      7.0Gi\n"
            "    Swap:          4.0Gi   256Mi   3.7Gi\n\n"
            "    $ cat /proc/meminfo | head -5\n"
            "    MemTotal:       16384000 kB\n"
            "    MemFree:         2201600 kB\n"
            "    MemAvailable:    7340032 kB\n"
            "    Buffers:          524288 kB\n"
            "    Cached:          5242880 kB\n\n"
            "    # Check swap usage\n"
            "    $ swapon --show\n"
            "    NAME      TYPE  SIZE  USED PRIO\n"
            "    /dev/sda2 partition 4G 256M   -2\n"
            "''')\n"
            "print(mem_output)"
        ),
    ]

    mcqs = [
        MCQ(
            question=(
                "In Round Robin scheduling with a time quantum of 5ms, "
                "what happens when a process has used its full quantum but "
                "hasn't finished?"
            ),
            options={
                "A": "The process is terminated",
                "B": "The process is moved to the back of the ready queue",
                "C": "The process continues running until completion",
                "D": "The process is moved to swap space",
            },
            correct="B",
            explanation=(
                "In Round Robin, when a process exhausts its time quantum, "
                "it is preempted and placed at the back of the ready queue. "
                "It will get another turn when all other processes have had "
                "their quantum."
            ),
            distractors={
                "A": "RR doesn't terminate processes — it preempts them and re-queues them.",
                "C": "That would be FCFS behavior, not Round Robin. RR enforces time slicing.",
                "D": "Swap is for memory management, not CPU scheduling.",
            },
            mcq_type="conceptual",
        ),
        MCQ(
            question="What is thrashing in the context of virtual memory?",
            options={
                "A": "A process using too much CPU time",
                "B": "Excessive page faults causing the system to spend most time swapping",
                "C": "A disk drive failing due to excessive writes",
                "D": "A network interface dropping packets under load",
            },
            correct="B",
            explanation=(
                "Thrashing occurs when the system spends more time handling "
                "page faults (swapping pages in and out of memory) than "
                "executing actual process instructions. It happens when "
                "physical memory is severely overcommitted."
            ),
            distractors={
                "A": "High CPU usage is not thrashing — thrashing is specifically about memory/swap.",
                "C": "Disk failure is a hardware issue, not related to virtual memory thrashing.",
                "D": "Packet dropping is a network issue, not a memory management concept.",
            },
            mcq_type="conceptual",
        ),
    ]

    return TopicSection(
        title="Process Scheduling and Memory Management",
        difficulty="Mid-Level",
        explanation=explanation,
        examples=examples,
        practice=mcqs,
        key_takeaways=[
            "Round Robin gives each process a fixed time quantum — fair but adds context-switch overhead.",
            "Priority scheduling can cause starvation; aging solves this by gradually increasing priority.",
            "Linux uses CFS (Completely Fair Scheduler) — nice values affect CPU share.",
            "Virtual memory + paging lets each process see its own address space.",
            "Thrashing = excessive swapping. Monitor with `free -h` and `vmstat`.",
        ],
    )


