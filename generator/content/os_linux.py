def _pxe_boot_overview() -> TopicSection:
    explanation = (
        "### PXE Boot Overview (NVIDIA-Relevant)\n\n"
        "**PXE (Preboot Execution Environment)** allows a computer to boot "
        "from the network instead of a local disk. It's widely used for:\n"
        "- Automated OS installation across server fleets\n"
        "- Diskless workstations\n"
        "- System recovery and diagnostics\n\n"
        "**PXE boot flow:**\n"
        "1. Client powers on and sends a DHCP Discover broadcast.\n"
        "2. DHCP server responds with an IP address AND the PXE boot server "
        "address + boot filename.\n"
        "3. Client downloads the boot loader (e.g., `pxelinux.0`) via **TFTP** "
        "(Trivial File Transfer Protocol).\n"
        "4. Boot loader downloads the kernel and initrd from the TFTP server.\n"
        "5. Kernel boots and may fetch additional files (kickstart/preseed) "
        "via HTTP/NFS for automated installation.\n\n"
        "**Key components:**\n"
        "- **DHCP server** — assigns IP and points to TFTP server\n"
        "- **TFTP server** — serves the boot loader and kernel\n"
        "- **HTTP/NFS server** — serves the OS installation files\n"
        "- **Kickstart (RHEL) / Preseed (Debian)** — automated install config\n\n"
        "**NVIDIA context:** PXE boot is used to provision bare-metal GPU "
        "servers at scale, deploy custom OS images with NVIDIA drivers, and "
        "manage server fleets in data centers."
    )

